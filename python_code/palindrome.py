def is_palindrome(sample):
    sample = sample.replace(' ', '').lower()
    if sample == sample[::-1]:
        print("It's a palindrome.")
    else:
        print("It's not a palindrome.")


sample_input = input("Enter a word, phrase or sentence: ")
is_palindrome(sample_input)
